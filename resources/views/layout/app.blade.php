    $('.nav-link').click(function(e) {
        e.preventDefault();
        var url = $(this).attr('href');
        var pieces = url.split('#');
        var target = pieces[1];
        if (pieces.length == 1) {
            window.scrollTo({
                top: 0,
                behavior: 'smooth'
            });
        } else {
            var element = document.querySelector("#" + target);

            // smooth scroll to element and align it at the bottom
            element.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }

    });
</script>
